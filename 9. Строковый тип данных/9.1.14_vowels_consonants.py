line, a, b = input().lower(), 0, 0
for i in range(0, len(line)):
    if line[i] in ('а', 'у', 'о', 'и', 'э', 'ы', 'я', 'ю', 'е'):
        a += 1
    elif line[i] in ('б', 'в', 'г', 'д', 'ж', 'з', 'й', 'к', 'л', 'м', 'н', 'п', 'р', 'с', 'т', 'ф', 'х', 'ц', 'ч', 'ш', 'щ'):
        b += 1
print("Количество гласных букв равно", a)
print('Количество согласных букв равно', b)